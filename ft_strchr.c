#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char ch;

	ch = (char)c;
	while (*s != ch && *s)
	{
		s++;
	}
	if (*s == ch)
		return ((char *)s);
	return (NULL);
}
